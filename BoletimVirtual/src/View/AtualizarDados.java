    public void readJTable(){
        
        DefaultTableModel modelo = (DefaultTableModel) jTableAtualizarDados.getModel();
          modelo.setNumRows(0);
        AlunoDAO aDAO = new AlunoDAO();
        
        for(Aluno a: aDAO.read()){
            
           modelo.addRow(new Object[]{
               a.getMatricula(),
               a.getNome(),
               a.getEndereco(),
               a.getNomeDaMae(),
               a.getNomeDoPai(),
               a.getCelular(),
               a.getCpf()
           });
        }
        
    }
    
